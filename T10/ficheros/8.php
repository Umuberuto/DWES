<h4>
    Realizar un contador de visitas en PHP siguiendo este esquema:<br>
    <ul>
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Crear en la carpeta htdocs, un fichero de texto "visitas.txt" y escribir
            en el un 0.
        </li>
        
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Abrir despu&eacute;s ese fichero en modo lectura/escritura.
        </li>
        
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Leer del fichero 8 bytes (u otra cantidad) y guardarlos en una variable
            que llamaremos $contador.
        </li>
        
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Sacar un mensaje por pantalla que indique: "Esta es la visita
            n&uacute;mero: $contador".
        </li>
        
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Incrementar el contador una unidad.
        </li>
        
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Colocar el puntero al inicio del fichero.
        </li>
        
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Escribir el nuevo valor del contador en el fichero.
        </li>
        
        &nbsp;&nbsp;&nbsp;&nbsp;
        <li>
            Cerrar el fichero.
        </li>
    </ul>
</h4>