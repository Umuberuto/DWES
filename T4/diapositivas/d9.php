<h4>&iquest;Qu&eacute; significa re-indexar?</h4>
<h5>
    RESPUESTA: Cuando borras las posiciones de un array (sin borrar su estructura)
    y reutilizas el mismo array vemos que al a&ntilde;adir elementos sin especificar
    las posiciones sigue tomando en cuenta las posiciones eliminadas aunque estas ya
    no existan. Para evitar esto se crea otro array en el que metas los elementos del
    primer array reordenados, es decir, reindexados.
</h5>